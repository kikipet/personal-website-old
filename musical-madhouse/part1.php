        <h2 style="padding: 1%">The right to play a symphony in the wrong key.</h2>
        <p>When, after eight or ten years of studies, I began to see the power of our great art, a student I knew
            approached me on
            behalf of the members of an amateur philharmonic, recently formed in the locale of Prado, to implore me to
            be their
            conductor. I had only ever conducted one piece of music, that in my first mass in the church of Saint
            Eustache. I was
            extremely wary of these amateurs; their orchestra had been and was deplorable. However, the idea of
            directing ensembles,
            experimenting, made me decide, and I accepted.</p>
        <p>When the day of the rehearsal arrived, I surrendered to Prado; I found around sixty musicians making that
            characteristically annoying noise of amateur orchestras. They were to play what?... A symphony in D by
            Gyrowetz. I
            believe only the boilermaker, tanner, Roman cook and Neapolitan barber have ever dreamt of similar
            platitudes. I resign,
            we begin. I hear an ugly dissonance coming from the clarinets. I interrupt the orchestra and address the
            clarinets:</p>
        <p>“You surely have the part for a different piece, gentlemen; we’re playing in D and you were just playing in
            F!”</p>
        <p>“No, sir, it is definitely the correct symphony!”</p>
        <p>“Let’s take it back from the top.”</p>
        <p>More dissonance, another stop.</p>
        <p>“But that’s impossible, show me your part.”</p>
        <p>Someone had the clarinet part passed to me.</p>
        <p>“By God! The discord explains itself. Your part is written in F, that’s true, but it’s for clarinets in A,
            and your F,
            in this case, should be in unison with our D. You’re using the wrong instrument.”</p>
        <p>“Sir, we only have clarinets in C.”</p>
        <p>“Well, transpose down a minor third.”</p>
        <p>“We don’t know how to transpose.”</p>
        <p>“Okay, then, don’t play.”</p>
        <p>“Oh really! We’re members of the society, and we have the same right to play as the others.”</p>
        <p>At these shocking words, dropping my baton, I ran like the devil was after me, and since then I never heard
            talk of
            these <i>philharmonics</i>.</p>
