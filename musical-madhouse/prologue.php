            last tie to
            hang himself.</p>
        <p>The existence of musicians in an orchestra is sowed in a way resembling a bit more that of roses than of the
            artists in
            choruses; they can give each other their hands. (les uns et les autres peuvent se donner la main.)
        </p>
        <p>Anyway, I would be happy, I can assure you, to soothe your boredom (to quote Molière’s Oronte); but the
            cheerfulness of
            my anecdotes are often problematic, and I wouldn’t dare give way to your friendly pleas, if the most
            depressing things
            didn’t so often have a buffoonish edge. You know what the condemned man says, in a hoarse voice to his
            tearful
            wife, who
            came to say her final farewells and to follow him to the scaffold:</p>
        <p>“You didn’t bring the little one?”</p>
        <p>“Ah! My God! How awful! Could I show him his father at the scaffold?”</p>
        <p>“You’re wrong, it would amuse him, that child.”</p>
        <p>Now, here’s an opuscule where I can’t distinguish the character too well; I’ll name it tentatively: The
            Musical Madhouse
            (Les Grotesques de la musique), as it has mad strangers to the musical art here and there. According to the
            spirit of
            the readers, it could seem ridiculous or deplorable. Try to find some pleasure in reading it; as for me, I
            amused myself
            writing it, undoubtedly like that child of the condemned attending his father’s execution.</p>
        <p>Farewell, ladies and gentlemen; I kiss the hands of the ladies and shake warmly the others, and I pray that
            you always
            believe in the sincere and alive affection from your devoted comrade,</p>
        <p>HECTOR BERLIOZ.</p>
        <p>Paris, January 21, 1859.</p>
