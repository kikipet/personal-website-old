        <h2 style="padding: 1%">The author's reply.</h2>
        <p>Ladies and gentlemen,</p>
        <p>You greet me: “Dear master”! I was considering responding: “Dear slaves”! For I know how much you’ve been
            stripped of
            leisure and freedom. Wasn’t I a singer back in the day? And at this theater too! God forbid that you enter
            here!</p>
        <p>So I know well the rough labors you complete, the number of (tristes heures) that you’ve counted, and the
            even less
            fortunate salary you are subjected to. Alas! I am not more master, nor more free, nor more joyful than you.
            You work, I
            work, we work to live; and you live, I live, we live to work. The Saint-simonians pretend to know the
            attractive job;
            they have guarded the secret well; I can assure you, that job is as foreign to me as to yourselves. I no
            longer count my
            (tristes heures); they come one after the other, cold and monotone like those drops of melted snow that
            weigh down on
            Paris in sombre silence on winter nights.</p>
        <p>As to my salary, don’t even mention it…</p>
        <p>I acknowledge the validity of your rebuke on the subject of the dedication of the <i>Soirées de
                l’Orchestre</i>; I should have,
            since it is a book on music and musicians, offered it to my friends the artists of Paris. But I came back to
            Germany
            when it struck my fancy to write that book; I was again under the effect of the warm and cordial welcome
            that civilized
            city’s orchestra gave me, and I supposed so little (ugh choppy) to find the least public sympathy for my
            Soirées, that
            to dedicate it to someone was, I felt, to put them under a patronage and not in the least an homage that one
            can be
            flattered by. Your objections to that proposition seem to show that you have a different opinion from mine.
            In your
            belief, there would actually be readers of my prose! I would be mistaken!... I would be an imbecile! That
            fills me with
            joy.</p>
        <p>You tease me for being picky about grammar. I hardly pretend to be an expert in French; I know you know what
            I don’t
            know. But a good number of common words are, I’ll be honest, barbarian, and I can’t stand hearing them. The
            word
            “angoise” is one of them; it is often used by the best paid singers who perform at our opera houses. One
            prize-winning
            student insisted, despite everyone else’s opinion, on saying, “Mortelle angoise!” I went so far as to tell
            her there
            were 3 s’s in the word, hoping that she would pronounce at least two. It worked: she finally sang, “Mortelle
            angoisse!”
        </p>
        <p>You seem to be jealous of the instrumentalists playing seated in the pit, as opposed to standing for long
            hours. Be
            fair. They’re seated, I admit, in the pit where they barely get water to drink, but they’re always playing,
            with no
            rest, no respite, no mercy, not any closer than you to the unrestrait of my friends of the civilized city.
            The directors
            only let them count rests, when be chance the composer gives them rests to count. They play in overtures,
            airs, duets,
            trios, quartets, ensemble pieces, they accompany your choruses; a manager at the Opéra would even like to
            have them play
            in the choruses without accompaniment, pretending that he doesn’t pay them for crossing their arms.
        </p>
        <p>And you know how they’re paid!
        </p>
        <p>They don’t change costumes every half-hour, that’s true; but the obligation to present themselves in a white
            tie is
            ruinous for them. There are some poor fellow musicians at the Opéra who earn, they say, about 66 fr. 65 c.
            per month. At
            fourteen concerts a month, that makes not even 5 fr. per five-hour session; it’s a bit less than 20 sous an
            hour, less
            than for a fiacre. And now they’re striking to be able to dress well. They need at least seven white ties a
            month,
            assuming that they know to keep several clean to use multiple times. And this starching will finish with
            producing a
            pretty round sum. How much does it cost to starch and iron a white tie (without counting the price of the
            tie itself)?
            Fifteen centimes. Let’s admit that the artist abstains, for financial reasons, to have his tie starched, and
            irons it
            for formal occasions only. Those fifteen centimes will then be reduced to two sous. Well, see, it’s
            necessary to write
            the following account at the end of the month:
