        <h2 style="padding: 1%">Letter from the Paris Opera singers to the author.</h2>
        <p>Dear master, </p>
        <p>You have dedicated a book (<i>Les Soirées de l’orchestre</i>) to your good friends at X***, “a civilized
            city.” This city (in Germany, we know,) probably isn’t more civilized than a lot of others, despite the
            mischievous intent in giving them that epithet. We’re allowed to speculate as to whether their artists are
            really better than ours, and as to their affection for you, it surely can’t be as lively or as old as ours.
            Parisian choruses in general, and those of the Opéra in particular, are devoted to you heart and soul;
            they’ve proven that to you many times in all sorts of ways. Have they ever grumbled about the length of the
            rehearsals, of the rigor of your musical demands, of your violent interrogations, even of your rage, while
            studying the <i>Requiem</i>, <i>Te Deum</i>, <i>Roméo et Juliette</i>, <i>La Damnation de Faust</i>,
            <i>L’Enfance du Christ</i>, etc.?... Never, never. On the contrary, they have always done their job with
            zeal and a steadfast patience. However, you’re not exactly flattering to the men or courteous to the ladies
            during these awful rehearsals.</p>
        <p>When it’s time to start, if the chorus isn’t all here, if someone’s missing, you pace in front of the piano
            like the lion at the Jardin des Plantes in his cage, you growl softly while biting your lower lip, you stare
            daggers at everyone; we greet you, you turn your head away; you sometimes violently hit dissonant chords on
            the piano to show your anger, and you tell us clearly that you are capable of tearing the latecomers and the
            absentees limb from limb… if they were here.</p>
        <p>Then you reproach us for not singing piano enough during the quiet passages, for not attacking the fortes
            together; you want us to pronounce both s’s in the word “angoisse” and the second r in the word “traître”.
            And if one unfortunate illiterate, just one, lost in our ranks, forgets your grammatical observation and
            risks saying “angoise” or “traite” one more time, you lash out at everyone, you overwhelm us with cruel
            jokes, calling us doormen, green room ushers, etc.!! Ah well, we support it nevertheless, and we love you
            all the same, because you love us, we see that, and we understand how much you adore music.
        </p>
        <p>Only the French habit of giving pre-eminence to foreigners could bring you to dedicate your <i>Soirées de
                l’orchestre</i> to German musicians, even though it is a gross injustice to do so.</p>
        <p>It’s done now, let’s not talk about it.</p>
        <p>But why couldn’t you write, at our request, a book of the same genre, maybe less philosophical, more
            cheerful, to dispel the boredom that gnaws at us at the Opéra?</p>
        <p>As you know, during the acts or scenes that don’t include a chorus, we are prisoners backstage. It’s gloomy
            like the entre-pont of a ship, it smells like lamp oil, we can’t sit down; we hear badly told, old moldy
            stories or rancid jokes; or the silence and inaction deaden our brains until we’re told to get on stage...
            Ah! it’s not a pretty job, believe us. Doing dozens of rehearsals to get almost unsingable new songs stuck
            in our heads! Memorizing operas that last from seven to midnight! Changing costume up to six times a night!
            Staying penned up like sheep, when we’re not singing, and not having even five minutes to ourselves during
            those endless performances!!... For we are not like your German artists, who let you perform the pieces
            where they don’t do very much at half-orchestra. We all sing
            all the time. Granted, if we then took the liberty to only sing in the parts we like, cases of angina would
            be rare among the Opéra chorus. In addition, we sing standing up, we’re always on our feet, even when the
            orchestra plays seated in their pit. C’est à devenir huître!</p>
        <p>So, be good, write us a book of real stories, of fabulous tales, of jokes even, like the ones you often write
            when you’re in a bad mood; we’ll read them during our breaks under the light of our lamps; we need to forget
            our sad times, and you will have the right to all of our acknowledgement.
        </p>
        <p>Your loyal sopranos, altos, tenors and basses of the Opéra.
        </p>
        <p>Paris, December 22, 1858.</p>
