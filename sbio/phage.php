        <p>Bacteriophages can also be split into lytic and lysogenic phages. Lytic phages kill their hosts immediately, while lysogenic phages can wait alongside their hosts for a while 
        (either by attaching to the membrane or inserting itself into the bacterium’s genome) until conditions get bad, in which case they escape and kill their hosts.
         In either case, the phage has to be pretty stable because it might not be able to find a host for a long time.</p>
